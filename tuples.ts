// Tuples allow you to define an array with a fixed number of elements, where each element can have a different type.
// You define a tuple using square brackets with the types of its elements.

// When to Use:
// Use tuples when you want to store a fixed number of elements with different types in a single array.
// Tuples are useful for grouping related values, like returning multiple values from a function.

let person: [string, number];
person = ["John", 25];

console.log(person[0]); 
console.log(person[1]); 
