// How to Use:
// Types are used by declaring them when defining variables, function parameters, or return types.
// You can use built-in types (string, number, boolean, etc.).

// When to Use:
// Use types to enforce the structure of your data and ensure type safety.
// This prevents errors by ensuring that variables, function arguments, and return values are of the expected type.

let isDone: boolean = false;    // Boolean type
let age: number = 25;           // Number type
let userName: string = "Alice"; // String type
let hobbies: string[] = ["Reading", "Gaming"]; // Array of strings
let notSure: any = 4;           // Any type can hold any value

console.log(isDone);   
console.log(age);     
console.log(userName); 
console.log(hobbies);  
console.log(notSure);  
